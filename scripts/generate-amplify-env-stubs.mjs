import fs from "node:fs";
import path from "node:path";

const functions = [
  "postConfirmation",
  "createPin",
  "reportPin",
  "recordClick",
  "listPinsInBounds",
];

const dataFunctions = new Set([
  "postConfirmation",
  "createPin",
  "reportPin",
  "recordClick",
  "listPinsInBounds",
]);

const outDir = path.join(process.cwd(), ".amplify", "generated", "env");

const genericStub = `// This file is auto-generated by Amplify. Edits will be overwritten.
export const env = process.env as any;
`;

function needsDataEnvRefresh(content) {
  return !content.includes("AMPLIFY_DATA_DEFAULT_NAME");
}

fs.mkdirSync(outDir, { recursive: true });

for (const name of functions) {
  const filePath = path.join(outDir, `${name}.ts`);
  const content = fs.existsSync(filePath) ? fs.readFileSync(filePath, "utf8") : "";

  if (dataFunctions.has(name)) {
    if (!content || needsDataEnvRefresh(content)) {
      fs.writeFileSync(filePath, genericStub);
    }
    continue;
  }

  if (!content) {
    fs.writeFileSync(filePath, genericStub);
  }
}
